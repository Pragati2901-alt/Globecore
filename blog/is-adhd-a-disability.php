                    <p><strong>Is ADHD a disability?</strong> If you’ve been asking this question, for yourself, your child, or someone you love, you’re not alone. Millions of people live with Attention Deficit Hyperactivity Disorder (ADHD) and wonder whether it qualifies as a disability under the law, what rights they have, and whether they can access support. The short answer is yes, ADHD can be considered a disability, but it depends on how significantly it affects daily life.</p>

                    <h2>Is ADHD a Disability? The Legal View</h2>
                    <p>Under U.S. law, ADHD can be classified as a disability if it substantially limits one or more major life activities. This is governed by several key pieces of legislation:</p>
                    
                    <h3>The Americans with Disabilities Act (ADA)</h3>
                    <p>The ADA prohibits discrimination against people with disabilities. ADHD can qualify if it significantly impacts areas like concentrating, thinking clearly, learning, or working. If ADHD limits these areas, you may be legally protected in the workplace and other public settings.</p>

                    <h3>Section 504 and IDEA</h3>
                    <p>For children in school, Section 504 and the Individuals with Disabilities Education Act (IDEA) provide frameworks for accommodations like extra time on tests, preferential seating, or Individualized Education Programs (IEPs).</p>

                    <h2>Common Symptoms & Impact</h2>
                    <p>ADHD is a neurodevelopmental condition that affects the brain's executive function. Common symptoms include:</p>
                    <ul>
                        <li>Difficulty staying focused or conversing.</li>
                        <li>Forgetfulness and losing items.</li>
                        <li>Impulsivity and restlessness.</li>
                        <li>Emotional dysregulation.</li>
                    </ul>

                    <h2>Support and Treatment Options</h2>
                    <p>ADHD is highly manageable. While it may be a disability, it doesn't have to be a barrier to success. Treatment often includes:</p>
                    <ul>
                        <li><strong>Medication:</strong> Stimulants and non-stimulants prescribed by a psychiatrist.</li>
                        <li><strong>Behavioral Therapy:</strong> CBT to build coping skills and time management.</li>
                        <li><strong>TMS Therapy:</strong> For some, <a href="https://globecoreinc.com/services/tms-therapy.php">TMS Therapy</a> offers a non-invasive way to stimulate brain regions that regulate focus.</li>
                    </ul>

                    <h2>When to Seek Professional Help</h2>
                    <p>If ADHD symptoms are interfering with work, school, or relationships, it's time to seek a formal evaluation. A diagnosis from a licensed <a href="../about/meet-our-team.php">psychiatrist</a> is the first step toward accessing legal protections and effective treatment.</p>

